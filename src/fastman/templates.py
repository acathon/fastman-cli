"""
Code templates for project generation.

Two rendering surfaces:

- :class:`Template` — render an inline string template (used by older
  templates that still live as Python string constants in this module).
- :class:`TemplateLoader` — load and render a ``.j2`` file shipped with the
  ``fastman`` package under ``_templates/``. Use this for new templates;
  it gets syntax highlighting, linter coverage, and supports
  ``{% include %}`` / ``{% extends %}`` between fragments.

Both surfaces use the same Jinja2 environment with autoescape OFF (we
generate Python source, not HTML) and ``StrictUndefined`` so a missing
context key fails loudly. Bare ``{`` / ``}`` characters pass through
unchanged — only ``{{ ... }}``, ``{% ... %}``, and ``{# ... #}`` are
interpreted.
"""
from typing import Any, Dict

from jinja2 import Environment, PackageLoader, StrictUndefined


# Shared environment for **inline** string templates — built once at import
# time, reused for every render. ``keep_trailing_newline=True`` preserves
# the final newline on generated files.
_jinja_env = Environment(
    autoescape=False,
    undefined=StrictUndefined,
    keep_trailing_newline=True,
    trim_blocks=False,
    lstrip_blocks=False,
)


# Environment for **file-backed** templates loaded from src/fastman/_templates/.
# Separate env (with its own PackageLoader) so ``{% include "x.j2" %}`` works
# inside .j2 files without leaking into inline rendering.
_jinja_file_env = Environment(
    loader=PackageLoader("fastman", "_templates"),
    autoescape=False,
    undefined=StrictUndefined,
    keep_trailing_newline=True,
    trim_blocks=False,
    lstrip_blocks=False,
)


class Template:
    """Jinja2-backed engine for inline string templates.

    Variable syntax is ``{{ name }}``. Conditionals and loops use
    ``{% if ... %}`` / ``{% for ... %}`` blocks — see the Jinja2 docs.
    """

    @staticmethod
    def render(template: str, context: Dict[str, Any]) -> str:
        """Render a template string with the given context."""
        return _jinja_env.from_string(template).render(**context)


class TemplateLoader:
    """Load and render ``.j2`` files shipped with the fastman package.

    Templates live under ``src/fastman/_templates/`` and are referenced by
    POSIX-style relative path (``"mail/base.py.j2"``). The loader is wired
    via ``jinja2.PackageLoader``, so packaging (``pip install fastman``)
    just works as long as ``_templates/**`` is included as package data.
    """

    @staticmethod
    def render(template_name: str, context: Dict[str, Any]) -> str:
        """Load ``_templates/<template_name>`` and render with `context`."""
        return _jinja_file_env.get_template(template_name).render(**context)


