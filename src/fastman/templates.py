def _import_all_models() -> None:
    """Import every model module so Base.metadata is populated.

    Alembic's autogenerate only sees what's already registered with
    Base.metadata at this point. Generated projects can place models in any
    of: app/models/, app/features/<x>/models.py, app/api/<x>/models.py.
    This walker imports them all so `alembic revision --autogenerate` picks
    up new tables without the user having to maintain a central import list.
    """
    project_root = Path(__file__).resolve().parents[1]
    candidates = []

    # Central models package
    models_pkg = project_root / "app" / "models"
    if models_pkg.is_dir():
        for f in sorted(models_pkg.glob("*.py")):
            if f.name == "__init__.py" or f.name.startswith("_"):
                continue
            candidates.append(f"app.models.{f.stem}")

    # Per-feature / per-API models
    for sub in ("features", "api"):
        base = project_root / "app" / sub
        if not base.is_dir():
            continue
        for child in sorted(base.iterdir()):
            if not child.is_dir() or child.name.startswith("_"):
                continue
            models_file = child / "models.py"
            if models_file.exists():
                candidates.append(f"app.{sub}.{child.name}.models")

    for module_path in candidates:
        try:
            importlib.import_module(module_path)
        except Exception as exc:  # pragma: no cover — best-effort discovery
            print(f"alembic: failed to import {module_path}: {exc}", file=sys.stderr)


